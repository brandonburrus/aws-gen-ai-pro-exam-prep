import { BedrockRuntimeClient, ConverseCommand } from '@aws-sdk/client-bedrock-runtime'

/**
 * Event shape received from Step Functions.
 * This is the direct output of the `invoke-template` Lambda, passed through
 * unchanged so that no intermediate mapping state is needed in the state machine.
 */
export interface ScoreOutputEvent {
  /** The model's generated text response to the question. */
  output: string
  /** The original question that was posed to the model. */
  question: string
  /**
   * Bedrock model ID that produced the output.
   * Echoed through to the result so downstream aggregation can dimension
   * scores by model without re-parsing the ARN.
   */
  model_id: string
  /**
   * Human-readable name of the prompt template that was used.
   * Echoed through for per-template aggregation.
   */
  template_name: string
  /** Input token count from the invoke-template call (pass-through). */
  input_tokens: number
  /** Output token count from the invoke-template call (pass-through). */
  output_tokens: number
}

/**
 * Result returned to Step Functions after scoring.
 * Carries all pass-through fields from the event plus the judge scores and
 * the judge's own token usage so the aggregation stage can account for total
 * pipeline cost.
 */
export interface ScoreOutputResult {
  /** Relevance score assigned by the judge model (1-5, inclusive). */
  relevance: number
  /** Consistency score assigned by the judge model (1-5, inclusive). */
  consistency: number
  /** The judge model's brief explanation justifying its scores. */
  reasoning: string
  /** Model ID that generated the scored output (pass-through). */
  model_id: string
  /** Template name used to generate the scored output (pass-through). */
  template_name: string
  /** Original question (pass-through). */
  question: string
  /** The model's answer that was scored (pass-through). */
  output: string
  /** Input tokens from the invoke-template call (pass-through). */
  input_tokens: number
  /** Output tokens from the invoke-template call (pass-through). */
  output_tokens: number
  /** Input tokens consumed by the judge Converse call. */
  judge_input_tokens: number
  /** Output tokens generated by the judge Converse call. */
  judge_output_tokens: number
}

/**
 * Internal shape of the structured JSON the judge model returns.
 */
interface JudgeScores {
  relevance: number
  consistency: number
  reasoning: string
}

/**
 * Bedrock model ID used as the evaluation judge.
 * Defaults to Nova Pro via the US cross-region inference profile.
 * Override with the `JUDGE_MODEL_ID` environment variable.
 */
const JUDGE_MODEL_ID = process.env.JUDGE_MODEL_ID ?? 'us.amazon.nova-pro-v1:0'

/**
 * System prompt sent to the judge model on every invocation.
 * Explicitly requests a raw JSON object so that Nova models (which do not
 * support the `outputConfig.textFormat` structured-outputs feature) return
 * parseable output rather than markdown prose.
 */
const JUDGE_SYSTEM_PROMPT =
  'You are an impartial evaluation judge. ' +
  'You will be given a question and an AI-generated answer. ' +
  'Score the answer on two dimensions:\n' +
  '- relevance (integer 1-5): Does the answer directly address the question? ' +
  '1 = completely off-topic, 5 = directly and fully addresses it.\n' +
  '- consistency (integer 1-5): Is the answer internally consistent and free of contradictions? ' +
  '1 = self-contradictory, 5 = fully coherent.\n\n' +
  'You MUST respond with ONLY a raw JSON object — no markdown, no prose, no code fences. ' +
  'The object must have exactly these three keys: "relevance", "consistency", "reasoning". ' +
  'Example of the required format:\n' +
  '{"relevance":4,"consistency":5,"reasoning":"The answer directly addresses the question and is internally coherent."}'

const runtimeClient = new BedrockRuntimeClient({ region: process.env.AWS_REGION ?? 'us-east-1' })

/**
 * Clamps a numeric value to the inclusive [1, 5] scoring range.
 *
 * @param value - The raw numeric value from the parsed judge response.
 * @returns The value clamped to [1, 5].
 */
function clampScore(value: number): number {
  return Math.max(1, Math.min(5, value))
}

/**
 * Strips markdown code fences from a string if present.
 * Nova models occasionally wrap JSON in ```json ... ``` blocks despite the
 * system prompt instructing them not to.
 *
 * @param text - Raw response text from the judge model.
 * @returns The text with leading/trailing fences and whitespace removed.
 */
function stripMarkdownFences(text: string): string {
  return text
    .replace(/^```(?:json)?\s*/i, '')
    .replace(/\s*```\s*$/, '')
    .trim()
}

/**
 * Extracts score integers from markdown-formatted judge output as a last-resort
 * fallback when the model ignores the JSON instruction entirely.
 *
 * Handles patterns like "**Relevance: 4**", "Relevance: 4", "relevance=4", etc.
 *
 * @param text - The raw response text from the judge model.
 * @returns Parsed scores if extractable, or null if the text cannot be parsed.
 */
function extractScoresFromMarkdown(text: string): JudgeScores | null {
  const relevanceMatch = text.match(/relevance[:\s*=_]+(\d)/i)
  const consistencyMatch = text.match(/consistency[:\s*=_]+(\d)/i)

  if (!relevanceMatch || !consistencyMatch) {
    return null
  }

  const relevance = parseInt(relevanceMatch[1], 10)
  const consistency = parseInt(consistencyMatch[1], 10)

  if (isNaN(relevance) || isNaN(consistency)) {
    return null
  }

  // Extract a reasoning sentence if one exists; fall back to a placeholder.
  const reasoningMatch = text.match(/reasoning[:\s]+(.+?)(?:\n|$)/i)
  const reasoning = reasoningMatch ? reasoningMatch[1].trim() : 'Extracted from non-JSON response.'

  return { relevance, consistency, reasoning }
}

/**
 * Attempts to parse judge scores from a raw response string.
 * Strategy (in order):
 * 1. Strip markdown fences and parse as JSON.
 * 2. Find and parse the first `{...}` JSON object embedded in the text.
 * 3. Extract scores via regex from markdown-formatted prose.
 *
 * @param raw - Raw response text from the judge model.
 * @returns Parsed `JudgeScores` object.
 * @throws Error if none of the three strategies succeeds.
 */
function parseJudgeResponse(raw: string): JudgeScores {
  const stripped = stripMarkdownFences(raw)

  // Strategy 1: direct JSON parse after fence removal.
  try {
    return JSON.parse(stripped) as JudgeScores
  } catch {
    // fall through
  }

  // Strategy 2: find the first {...} block embedded in the response.
  const jsonMatch = stripped.match(/\{[\s\S]*?\}/)
  if (jsonMatch) {
    try {
      return JSON.parse(jsonMatch[0]) as JudgeScores
    } catch {
      // fall through
    }
  }

  // Strategy 3: regex extraction from markdown prose.
  const extracted = extractScoresFromMarkdown(raw)
  if (extracted) {
    return extracted
  }

  throw new Error(
    `Unable to extract scores from judge response. ` +
      `Raw response (first 300 chars): "${raw.slice(0, 300)}"`,
  )
}

/**
 * Lambda handler invoked by Step Functions for each invoke-template result
 * in the evaluation pipeline.
 *
 * Execution flow:
 * 1. Validate that the required `output` and `question` fields are present.
 * 2. Build a user message combining the question and the model's answer.
 * 3. Call the judge model via the Bedrock Converse API.
 * 4. Parse the response using a three-strategy fallback chain.
 * 5. Clamp the integer scores defensively and return the full result.
 *
 * @param event - The Step Functions task input, which is the direct output of
 *   the `invoke-template` Lambda.
 * @returns Scores, reasoning, pass-through metadata, and judge token usage.
 * @throws Error on missing required fields, Converse API failures, or
 *   unparseable judge responses.
 */
export async function handler(event: ScoreOutputEvent): Promise<ScoreOutputResult> {
  const { output, question, model_id, template_name, input_tokens, output_tokens } = event

  if (!output || !question) {
    throw new Error(
      `score-output requires non-empty "output" and "question" fields. ` +
        `Received: output="${output?.slice(0, 80)}", question="${question?.slice(0, 80)}"`,
    )
  }

  const userMessage = `Question: ${question}\n\nAnswer: ${output}`

  let rawResponseText: string
  let judgeInputTokens: number
  let judgeOutputTokens: number

  try {
    const converseResp = await runtimeClient.send(
      new ConverseCommand({
        modelId: JUDGE_MODEL_ID,
        system: [{ text: JUDGE_SYSTEM_PROMPT }],
        messages: [
          {
            role: 'user',
            content: [{ text: userMessage }],
          },
        ],
        inferenceConfig: {
          // Temperature 0 maximises determinism for scoring reproducibility.
          maxTokens: 512,
          temperature: 0.0,
          topP: 1.0,
        },
      }),
    )

    rawResponseText = converseResp.output?.message?.content?.[0]?.text ?? ''
    judgeInputTokens = converseResp.usage?.inputTokens ?? 0
    judgeOutputTokens = converseResp.usage?.outputTokens ?? 0
  } catch (err) {
    throw new Error(
      `Judge Converse failed for question "${question.slice(0, 80)}": ${(err as Error).message}`,
      { cause: err },
    )
  }

  let scores: JudgeScores

  try {
    scores = parseJudgeResponse(rawResponseText)
  } catch (err) {
    throw new Error(
      `Failed to parse judge response for question "${question.slice(0, 80)}": ` +
        `${(err as Error).message}`,
      { cause: err },
    )
  }

  return {
    relevance: clampScore(scores.relevance),
    consistency: clampScore(scores.consistency),
    reasoning: scores.reasoning,
    model_id,
    template_name,
    question,
    output,
    input_tokens,
    output_tokens,
    judge_input_tokens: judgeInputTokens,
    judge_output_tokens: judgeOutputTokens,
  }
}
