import { CloudWatchClient, PutMetricDataCommand, StandardUnit } from '@aws-sdk/client-cloudwatch'
import { S3Client, PutObjectCommand } from '@aws-sdk/client-s3'

/**
 * A single scored evaluation item received from the Step Functions state
 * machine. Represents the direct output of the `score-output` Lambda and
 * carries all pass-through fields from the `invoke-template` Lambda as well.
 *
 * This interface intentionally duplicates the `ScoreOutputResult` shape from
 * the `score-output` handler rather than importing from it. Each Lambda is
 * bundled independently by esbuild via `NodejsFunction`, so cross-Lambda
 * imports would introduce coupling without any runtime benefit.
 */
export interface ScoredItem {
  /** Relevance score assigned by the judge model (1–5, inclusive). */
  relevance: number
  /** Consistency score assigned by the judge model (1–5, inclusive). */
  consistency: number
  /** The judge model's brief explanation justifying its scores. */
  reasoning: string
  /** Bedrock model ID that generated the scored output. */
  model_id: string
  /** Human-readable name of the prompt template that was used. */
  template_name: string
  /** The original question posed to the model. */
  question: string
  /** The model's generated text response. */
  output: string
  /** Input tokens consumed by the invoke-template Converse call. */
  input_tokens: number
  /** Output tokens generated by the invoke-template Converse call. */
  output_tokens: number
  /** Input tokens consumed by the judge Converse call. */
  judge_input_tokens: number
  /** Output tokens generated by the judge Converse call. */
  judge_output_tokens: number
}

/**
 * Average relevance and consistency scores for a single grouping key
 * (either a template name or a model ID).
 */
export interface AverageScores {
  /** Mean relevance score across all items in the group (rounded to 2 dp). */
  relevance: number
  /** Mean consistency score across all items in the group (rounded to 2 dp). */
  consistency: number
  /** Number of scored items in this group. */
  count: number
}

/**
 * The value returned to Step Functions by the aggregate-results Lambda.
 * Contains per-template and per-model averages, total token costs, and a
 * reference to the S3 object holding the complete evaluation results.
 */
export interface AggregateResultsOutput {
  /** Average scores keyed by `template_name`. */
  by_template: Record<string, AverageScores>
  /** Average scores keyed by `model_id`. */
  by_model: Record<string, AverageScores>
  /** Total number of scored items that were aggregated. */
  total_items: number
  /** S3 object key where the full results JSON was written. */
  s3_key: string
  /** Sum of all `input_tokens` across the invoke-template calls. */
  total_input_tokens: number
  /** Sum of all `output_tokens` across the invoke-template calls. */
  total_output_tokens: number
  /** Sum of all `judge_input_tokens` across the score-output calls. */
  total_judge_input_tokens: number
  /** Sum of all `judge_output_tokens` across the score-output calls. */
  total_judge_output_tokens: number
}

/** CloudWatch namespace for all evaluation metrics, per the project spec. */
const METRIC_NAMESPACE = 'GenAI/PromptEvaluation'

/** S3 key for the full evaluation results object, per the project spec. */
const S3_KEY = 'evaluation-results.json'

const RESULTS_BUCKET_NAME = process.env.RESULTS_BUCKET_NAME ?? ''

const cloudwatchClient = new CloudWatchClient({ region: process.env.AWS_REGION ?? 'us-east-1' })
const s3Client = new S3Client({ region: process.env.AWS_REGION ?? 'us-east-1' })

/**
 * Computes average relevance and consistency scores for each unique value of
 * `groupBy` found across the supplied items.
 *
 * @param items - The full array of scored evaluation items.
 * @param groupBy - The field name to group by: `'template_name'` or
 *   `'model_id'`.
 * @returns A record mapping each group key to its `AverageScores`. Relevance
 *   and consistency values are rounded to two decimal places.
 */
function computeAverages(
  items: ScoredItem[],
  groupBy: 'template_name' | 'model_id',
): Record<string, AverageScores> {
  const accumulator = new Map<
    string,
    { relevanceSum: number; consistencySum: number; count: number }
  >()

  for (const item of items) {
    const key = item[groupBy]
    const existing = accumulator.get(key)
    if (existing) {
      existing.relevanceSum += item.relevance
      existing.consistencySum += item.consistency
      existing.count += 1
    } else {
      accumulator.set(key, {
        relevanceSum: item.relevance,
        consistencySum: item.consistency,
        count: 1,
      })
    }
  }

  const result: Record<string, AverageScores> = {}
  for (const [key, { relevanceSum, consistencySum, count }] of accumulator) {
    result[key] = {
      relevance: Math.round((relevanceSum / count) * 100) / 100,
      consistency: Math.round((consistencySum / count) * 100) / 100,
      count,
    }
  }
  return result
}

/**
 * Publishes `Relevance` and `Consistency` metric data points to CloudWatch
 * under the `GenAI/PromptEvaluation` namespace. Each metric is emitted with
 * two separate dimension sets: one dimensioned by `TemplateName` and one
 * dimensioned by `ModelId`.
 *
 * All data points are published in a single `PutMetricData` call. At most
 * 3 templates × 2 metrics + 2 models × 2 metrics = 10 data points, well
 * within the 1000-point-per-call limit.
 *
 * @param byTemplate - Per-template average scores from `computeAverages`.
 * @param byModel - Per-model average scores from `computeAverages`.
 * @throws Error if the `PutMetricData` call fails.
 */
async function publishMetrics(
  byTemplate: Record<string, AverageScores>,
  byModel: Record<string, AverageScores>,
): Promise<void> {
  const metricData: Array<{
    MetricName: string
    Dimensions: Array<{ Name: string; Value: string }>
    Value: number
    Unit: StandardUnit
  }> = []

  for (const [templateName, avg] of Object.entries(byTemplate)) {
    metricData.push(
      {
        MetricName: 'Relevance',
        Dimensions: [{ Name: 'TemplateName', Value: templateName }],
        Value: avg.relevance,
        Unit: StandardUnit.None,
      },
      {
        MetricName: 'Consistency',
        Dimensions: [{ Name: 'TemplateName', Value: templateName }],
        Value: avg.consistency,
        Unit: StandardUnit.None,
      },
    )
  }

  for (const [modelId, avg] of Object.entries(byModel)) {
    metricData.push(
      {
        MetricName: 'Relevance',
        Dimensions: [{ Name: 'ModelId', Value: modelId }],
        Value: avg.relevance,
        Unit: StandardUnit.None,
      },
      {
        MetricName: 'Consistency',
        Dimensions: [{ Name: 'ModelId', Value: modelId }],
        Value: avg.consistency,
        Unit: StandardUnit.None,
      },
    )
  }

  try {
    await cloudwatchClient.send(
      new PutMetricDataCommand({
        Namespace: METRIC_NAMESPACE,
        MetricData: metricData,
      }),
    )
  } catch (err) {
    throw new Error(
      `PutMetricData failed for namespace "${METRIC_NAMESPACE}": ${(err as Error).message}`,
      { cause: err },
    )
  }
}

/**
 * Writes the full evaluation results to S3 as a pretty-printed JSON object
 * containing both the aggregation summary and every individual scored item.
 *
 * The object is written to the bucket specified by `RESULTS_BUCKET_NAME` at
 * the fixed key `evaluation-results.json`.
 *
 * @param items - The complete array of scored items to persist.
 * @param summary - The aggregated summary (excluding `s3_key`, which is
 *   added by the caller after this function returns).
 * @throws Error if the `PutObject` call fails or `RESULTS_BUCKET_NAME` is
 *   not set (validated by the caller before this function is invoked).
 */
async function writeResultsToS3(
  items: ScoredItem[],
  summary: Omit<AggregateResultsOutput, 's3_key'>,
): Promise<void> {
  const fullOutput = { summary, results: items }

  try {
    await s3Client.send(
      new PutObjectCommand({
        Bucket: RESULTS_BUCKET_NAME,
        Key: S3_KEY,
        Body: JSON.stringify(fullOutput, null, 2),
        ContentType: 'application/json',
      }),
    )
  } catch (err) {
    throw new Error(
      `PutObject failed for s3://${RESULTS_BUCKET_NAME}/${S3_KEY}: ${(err as Error).message}`,
      { cause: err },
    )
  }
}

/**
 * Lambda handler invoked by Step Functions as the final stage of the
 * evaluation pipeline.
 *
 * Execution flow:
 * 1. Validate that the event is a non-empty `ScoredItem[]` and that the
 *    `RESULTS_BUCKET_NAME` environment variable is set.
 * 2. Compute per-template and per-model average scores via `computeAverages`.
 * 3. Accumulate total token counts across all items in a single pass.
 * 4. Publish CloudWatch metrics and write the full results to S3 in parallel
 *    via `Promise.all`, since both operations are independent.
 * 5. Return the `AggregateResultsOutput` summary to Step Functions.
 *
 * @param event - The full array of `ScoredItem` objects collected from all
 *   branches of the Step Functions Map state (3 templates × 2 models × 5
 *   questions = 30 items in the nominal pipeline run).
 * @returns Aggregated averages, token totals, and the S3 key where full
 *   results were persisted.
 * @throws Error on validation failures, CloudWatch `PutMetricData` errors,
 *   or S3 `PutObject` errors. Step Functions task-level retry policies handle
 *   transient throttling.
 */
export async function handler(event: ScoredItem[] | ScoredItem[][]): Promise<AggregateResultsOutput> {
  if (!Array.isArray(event) || event.length === 0) {
    throw new Error(
      'aggregate-results received an empty or non-array event. Expected ScoredItem[].',
    )
  }

  if (!RESULTS_BUCKET_NAME) {
    throw new Error('RESULTS_BUCKET_NAME environment variable is not set.')
  }

  // The outer Map state produces ScoredItem[][] (one sub-array per question).
  // Flatten defensively so the rest of the handler works with a uniform ScoredItem[].
  const items: ScoredItem[] = Array.isArray(event[0])
    ? (event as ScoredItem[][]).flat()
    : (event as ScoredItem[])

  if (items.length === 0) {
    throw new Error('aggregate-results received an event that flattened to an empty array.')
  }

  // Step 2: Compute per-group averages.
  const byTemplate = computeAverages(items, 'template_name')
  const byModel = computeAverages(items, 'model_id')

  // Step 3: Accumulate token totals in a single pass.
  let totalInputTokens = 0
  let totalOutputTokens = 0
  let totalJudgeInputTokens = 0
  let totalJudgeOutputTokens = 0

  for (const item of items) {
    totalInputTokens += item.input_tokens
    totalOutputTokens += item.output_tokens
    totalJudgeInputTokens += item.judge_input_tokens
    totalJudgeOutputTokens += item.judge_output_tokens
  }

  const summary: Omit<AggregateResultsOutput, 's3_key'> = {
    by_template: byTemplate,
    by_model: byModel,
    total_items: items.length,
    total_input_tokens: totalInputTokens,
    total_output_tokens: totalOutputTokens,
    total_judge_input_tokens: totalJudgeInputTokens,
    total_judge_output_tokens: totalJudgeOutputTokens,
  }

  // Step 4: Publish metrics and persist results concurrently.
  await Promise.all([publishMetrics(byTemplate, byModel), writeResultsToS3(items, summary)])

  // Step 5: Return the summary to Step Functions.
  return { ...summary, s3_key: S3_KEY }
}
