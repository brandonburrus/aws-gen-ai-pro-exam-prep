import { BedrockAgentClient, GetPromptCommand } from '@aws-sdk/client-bedrock-agent'
import { BedrockRuntimeClient, ConverseCommand } from '@aws-sdk/client-bedrock-runtime'

/**
 * Event shape expected from Step Functions when invoking this Lambda.
 * One invocation corresponds to a single template + model combination.
 */
export interface InvokeTemplateEvent {
  /**
   * Full versioned ARN of the Bedrock Prompt Management resource.
   * Example: "arn:aws:bedrock:us-east-1:045064753163:prompt/ABC1234567:1"
   * The version number is encoded in the ARN after the final colon.
   */
  prompt_arn: string

  /**
   * Map of template variable names to their string values.
   * Must include every variable declared in the prompt template
   * (e.g. { role: "...", context: "...", question: "..." }).
   */
  variables: Record<string, string>

  /**
   * Bedrock model ID to invoke for this run.
   * Decoupled from the model baked into the prompt so the same template
   * can be evaluated against multiple models (e.g. Haiku vs Sonnet).
   * Example: "anthropic.claude-3-haiku-20240307-v1:0"
   */
  model_id: string

  /**
   * Human-readable name of the prompt template being evaluated.
   * Passed through to downstream scoring and aggregation stages so
   * they can dimension results without re-parsing the ARN.
   * Example: "direct-instruction"
   */
  template_name: string
}

/**
 * Result returned to Step Functions after a successful invocation.
 * Downstream Lambda stages receive this object and annotate it with scores.
 */
export interface InvokeTemplateResult {
  /** The model's generated text response after variable substitution. */
  output: string
  /** Number of input tokens consumed by the Converse call. */
  input_tokens: number
  /** Number of output tokens generated by the Converse call. */
  output_tokens: number
  /** Model ID that was invoked (echoed for downstream use). */
  model_id: string
  /** Template name (echoed for downstream use). */
  template_name: string
  /**
   * The value of the `question` variable from the event.
   * Passed through so the scoring Lambda can include the original
   * question in its judge prompt without the caller needing to
   * re-attach it.
   */
  question: string
}

/**
 * Parsed components extracted from a Bedrock Prompt Management versioned ARN.
 */
interface ParsedPromptArn {
  /** The 10-character alphanumeric prompt identifier. */
  promptId: string
  /** The version string, e.g. "1", "2", or "DRAFT". */
  version: string
}

const agentClient = new BedrockAgentClient({ region: process.env.AWS_REGION ?? 'us-east-1' })
const runtimeClient = new BedrockRuntimeClient({ region: process.env.AWS_REGION ?? 'us-east-1' })

/**
 * Parses a versioned Bedrock Prompt Management ARN into its constituent prompt
 * ID and version number.
 *
 * Expected ARN format:
 * `arn:aws:bedrock:<region>:<account>:prompt/<promptId>:<version>`
 *
 * @param arn - The full versioned prompt ARN.
 * @returns An object containing the `promptId` and `version` strings.
 * @throws Error if the ARN does not match the expected format.
 */
function parsePromptArn(arn: string): ParsedPromptArn {
  // Split on "/" to isolate the "prompt/<id>:<version>" tail.
  const parts = arn.split('/')
  if (parts.length < 2) {
    throw new Error(`Invalid prompt ARN format: "${arn}"`)
  }

  const tail = parts[parts.length - 1]
  const colonIdx = tail.lastIndexOf(':')
  if (colonIdx === -1) {
    throw new Error(
      `Prompt ARN does not contain a version number: "${arn}". ` +
        'Supply a versioned ARN (e.g. arn:...prompt/ABC1234567:1).',
    )
  }

  const promptId = tail.slice(0, colonIdx)
  const version = tail.slice(colonIdx + 1)

  if (!promptId || !version) {
    throw new Error(`Could not extract promptId or version from ARN: "${arn}"`)
  }

  return { promptId, version }
}

/**
 * Replaces all `{{variableName}}` placeholders in the template string with
 * the corresponding values from the supplied map.
 *
 * After substitution, logs a warning for any placeholders that remain
 * unresolved (i.e. keys present in the template but absent from `variables`).
 *
 * @param template - Raw template string containing `{{...}}` placeholders.
 * @param variables - Map of variable names to replacement values.
 * @returns The template string with all matched placeholders replaced.
 */
function substituteVariables(template: string, variables: Record<string, string>): string {
  let result = template
  for (const [key, value] of Object.entries(variables)) {
    result = result.replaceAll(`{{${key}}}`, value)
  }

  const unresolved = result.match(/\{\{[^}]+\}\}/g)
  if (unresolved) {
    console.warn(
      `[invoke-template] Unresolved template placeholders after substitution: ${unresolved.join(', ')}`,
    )
  }

  return result
}

/**
 * Lambda handler invoked by Step Functions for each template + model
 * combination in the evaluation pipeline.
 *
 * Execution flow:
 * 1. Parse the versioned prompt ARN into its ID and version components.
 * 2. Fetch the prompt template definition from Bedrock Prompt Management
 *    using `GetPrompt`.
 * 3. Substitute the supplied variables into the template text.
 * 4. Invoke the specified model via the Bedrock Converse API, using the
 *    inference configuration stored on the prompt variant.
 * 5. Return the generated output and token usage to Step Functions.
 *
 * @param event - The Step Functions task input containing the prompt ARN,
 *   variables, model ID, and template name.
 * @returns The model output text plus token counts and pass-through metadata.
 * @throws Error on GetPrompt failures, missing template configuration, or
 *   Converse API errors. Step Functions task-level retry policies handle
 *   transient throttling.
 */
export async function handler(event: InvokeTemplateEvent): Promise<InvokeTemplateResult> {
  const { prompt_arn, variables, model_id, template_name } = event

  // Step 1: Parse the versioned ARN.
  const { promptId, version } = parsePromptArn(prompt_arn)

  // Step 2: Fetch the prompt template from Bedrock Prompt Management.
  let templateText: string
  let inferenceMaxTokens: number
  let inferenceTemperature: number
  let inferenceTopP: number

  try {
    const getPromptResp = await agentClient.send(
      new GetPromptCommand({
        promptIdentifier: promptId,
        promptVersion: version,
      }),
    )

    const variant = getPromptResp.variants?.[0]
    if (!variant) {
      throw new Error(`Prompt "${promptId}" version "${version}" has no variants.`)
    }

    if (variant.templateType !== 'TEXT') {
      throw new Error(
        `Unsupported template type "${variant.templateType}" on prompt "${promptId}". ` +
          'Only TEXT templates are supported by this Lambda.',
      )
    }

    const textConfig = variant.templateConfiguration?.text
    if (!textConfig?.text) {
      throw new Error(`Prompt "${promptId}" version "${version}" has no template text.`)
    }

    templateText = textConfig.text
    inferenceMaxTokens = variant.inferenceConfiguration?.text?.maxTokens ?? 1024
    inferenceTemperature = variant.inferenceConfiguration?.text?.temperature ?? 0.2
    inferenceTopP = variant.inferenceConfiguration?.text?.topP ?? 0.9
  } catch (err) {
    throw new Error(
      `GetPrompt failed for ARN "${prompt_arn}": ${(err as Error).message}`,
      { cause: err },
    )
  }

  // Step 3: Substitute variables into the template text.
  const renderedPrompt = substituteVariables(templateText, variables)

  // Step 4: Invoke the model via the Bedrock Converse API.
  // The model_id from the event overrides whatever model is stored on the
  // prompt variant, enabling cross-model evaluation of the same template.
  let output: string
  let input_tokens: number
  let output_tokens: number

  try {
    const converseResp = await runtimeClient.send(
      new ConverseCommand({
        modelId: model_id,
        messages: [
          {
            role: 'user',
            content: [{ text: renderedPrompt }],
          },
        ],
        inferenceConfig: {
          maxTokens: inferenceMaxTokens,
          temperature: inferenceTemperature,
          topP: inferenceTopP,
        },
      }),
    )

    output = converseResp.output?.message?.content?.[0]?.text ?? ''
    input_tokens = converseResp.usage?.inputTokens ?? 0
    output_tokens = converseResp.usage?.outputTokens ?? 0
  } catch (err) {
    throw new Error(
      `Converse failed for model "${model_id}" with template "${template_name}": ${(err as Error).message}`,
      { cause: err },
    )
  }

  return {
    output,
    input_tokens,
    output_tokens,
    model_id,
    template_name,
    question: variables.question ?? '',
  }
}
